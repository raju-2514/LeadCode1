package April.Binary_Tree_inOrder_Traversal;

public class TreeNode {
    int val;
    Solution.TreeNode left;
    Solution.TreeNode right;

    TreeNode() {
    }

    TreeNode(int val) {
        this.val = val;
    }

    TreeNode(int val, Solution.TreeNode left, Solution.TreeNode right) {
        this.val = val;
        this.left = left;
        this.right = right;
    }


}
