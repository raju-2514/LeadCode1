package February.DeleteNodeintheLinkedList;

public class ListNode {
    int val;
    ListNode left;
    ListNode right;
    ListNode next;

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode left, ListNode right) {
        this.val = val;
        this.left = left;
        this.right = right;
    }
}
